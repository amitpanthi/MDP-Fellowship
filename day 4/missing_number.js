function findMissingNum(array){
    let upper_number = array.length + 1
    let totalSum = upper_number * (upper_number + 1) / 2
    let arraySum = 0

    for(let iter = 0; iter < array.length; iter++){
        arraySum += array[iter]
    }
    return totalSum - arraySum
}

